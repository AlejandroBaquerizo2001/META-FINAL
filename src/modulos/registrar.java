    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        }else{
          /*int id= Integer.parseInt(tabla.getValueAt(fila, 0) .toString());*/
          String id= tabla.getValueAt(fila, 0) .toString();
          String dni= tabla.getValueAt(fila, 1) .toString();
          String nombre= tabla.getValueAt(fila, 2) .toString();
          String apellido= tabla.getValueAt(fila, 3) .toString();
          String edad= tabla.getValueAt(fila, 4) .toString();
          String cedula= tabla.getValueAt(fila, 5) .toString();
          String celular= tabla.getValueAt(fila, 6) .toString();
          String direccion= tabla.getValueAt(fila, 7) .toString();
          String user= tabla.getValueAt(fila, 8) .toString();
          txtId.setText(id);
          txtDni.setText(dni);
          txtNombre.setText(nombre);
          txtApellido.setText(apellido);
          txtEdad.setText(edad);
          txtCedula.setText(cedula);
          txtCelular.setText(celular);
          txtDireccion.setText(direccion);
          txtUser.setText(user);
        }
    }    
        
