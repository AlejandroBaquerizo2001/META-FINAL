        String edad=txtEdad.getText();
        String cedula=txtCedula.getText();
        String celular=txtCelular.getText();
        String direccion=txtDireccion.getText();
        String user=txtUser.getText();
        Object[] ob=new Object[8];
        ob[0]=id;
        ob[1]=dni;
        ob[2]=nombre;
        ob[3]=apellido;
